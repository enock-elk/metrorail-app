// --- UTILITY: EDGE-COMPATIBLE JWT SIGNER FOR GOOGLE OAUTH ---
async function getGoogleAccessToken(clientEmail, privateKey) {
    const header = { alg: 'RS256', typ: 'JWT' };
    const now = Math.floor(Date.now() / 1000);
    const payload = {
        iss: clientEmail,
        scope: 'https://www.googleapis.com/auth/analytics.readonly',
        aud: 'https://oauth2.googleapis.com/token',
        exp: now + 3600,
        iat: now
    };

    const toBase64Url = (obj) => btoa(JSON.stringify(obj)).replace(/=/g, '').replace(/\+/g, '-').replace(/\//g, '_');
    const dataToSign = `${toBase64Url(header)}.${toBase64Url(payload)}`;

    const cleanKey = privateKey
        .replace(/\\n/g, '') 
        .replace(/\\r/g, '') 
        .replace(/"/g, '')   
        .replace(/-----BEGIN PRIVATE KEY-----/gi, '')
        .replace(/-----END PRIVATE KEY-----/gi, '')
        .replace(/\s/g, ''); 

    let binaryDerString;
    try {
        binaryDerString = atob(cleanKey);
    } catch (e) {
        throw new Error("Private Key Format Error: Could not decode Base64. Ensure you copied the entire key.");
    }

    const binaryDer = new Uint8Array(binaryDerString.length);
    for (let i = 0; i < binaryDerString.length; i++) {
        binaryDer[i] = binaryDerString.charCodeAt(i);
    }

    const cryptoKey = await crypto.subtle.importKey(
        'pkcs8',
        binaryDer.buffer,
        { name: 'RSASSA-PKCS1-v1_5', hash: 'SHA-256' },
        false,
        ['sign']
    );

    const signature = await crypto.subtle.sign('RSASSA-PKCS1-v1_5', cryptoKey, new TextEncoder().encode(dataToSign));
    const encodedSignature = btoa(String.fromCharCode(...new Uint8Array(signature))).replace(/=/g, '').replace(/\+/g, '-').replace(/\//g, '_');
    
    const jwt = `${dataToSign}.${encodedSignature}`;

    const response = await fetch('https://oauth2.googleapis.com/token', {
        method: 'POST',
        headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
        body: `grant_type=urn%3Aietf%3Aparams%3Aoauth%3Agrant-type%3Ajwt-bearer&assertion=${jwt}`
    });

    const data = await response.json();
    if (!response.ok) throw new Error(`Google Auth Failed: ${data.error_description || data.error}`);
    return data.access_token;
}

export default {
    async fetch(request, env, ctx) {
        // --- 1. CORS CONFIGURATION ---
        const corsHeaders = {
            'Access-Control-Allow-Origin': '*',
            'Access-Control-Allow-Methods': 'GET, OPTIONS',
            'Access-Control-Allow-Headers': 'Content-Type, Authorization',
            'Access-Control-Max-Age': '86400',
        };

        if (request.method === 'OPTIONS') {
            return new Response(null, { headers: corsHeaders });
        }

        const url = new URL(request.url);

        // --- 1.5 OPENGRAPH SOCIAL BOT INTERCEPTOR (GROWTH PHASE 7) ---
        const userAgent = request.headers.get('user-agent') || '';
        const isBot = /bot|facebookexternalhit|WhatsApp|Twitterbot|Applebot|Slackbot|TelegramBot|Discordbot/i.test(userAgent);

        // If a social bot hits the URL and there is a route parameter, intercept and serve the OpenGraph shell!
        if (isBot && url.searchParams.has('route')) {
            const routeId = url.searchParams.get('route');
            
            // Map to Firebase Storage Public Bucket for the PNG snapshots generated by the Admin Factory
            const bucket = "metrorail-next-train.firebasestorage.app";
            const imageUrl = `https://firebasestorage.googleapis.com/v0/b/${bucket}/o/og_images%2F${routeId}.png?alt=media`;

            const htmlShell = `<!DOCTYPE html>
            <html lang="en">
            <head>
                <meta charset="UTF-8">
                <meta property="og:title" content="Metrorail Next Train Schedule">
                <meta property="og:description" content="View the full digital timetable for this route.">
                <meta property="og:image" content="${imageUrl}">
                <meta property="og:image:width" content="1200">
                <meta property="og:image:height" content="630">
                <meta property="og:url" content="${request.url}">
                <meta property="og:type" content="website">
                <meta name="twitter:card" content="summary_large_image">
                <meta name="twitter:title" content="Metrorail Next Train Schedule">
                <meta name="twitter:description" content="View the full digital timetable for this route.">
                <meta name="twitter:image" content="${imageUrl}">
            </head>
            <body>
                <script>window.location.replace("/?route=${routeId}");</script>
            </body>
            </html>`;

            return new Response(htmlShell, {
                status: 200,
                headers: { 'Content-Type': 'text/html; charset=utf-8', 'Cache-Control': 'public, max-age=86400' }
            });
        }

        // --- NEW: IP GEOLOCATION ECHO ENDPOINT (GUARDIAN PHASE 5) ---
        if (url.pathname === '/region' && request.method === 'GET') {
            let region = 'GP'; // Default to Gauteng
            
            // Cloudflare automatically injects the 'cf' object containing geospatial data
            if (request.cf && request.cf.regionCode) {
                const prov = request.cf.regionCode.toUpperCase();
                if (prov === 'WC') region = 'WC';
                else if (prov === 'KZN' || prov === 'NL') region = 'KZN'; // NL = Natal
                else if (prov === 'EC') region = 'EC';
            }
            
            return new Response(JSON.stringify({ region }), {
                status: 200,
                headers: { ...corsHeaders, 'Content-Type': 'application/json' }
            });
        }

        if (request.method !== 'GET') {
            return new Response('Method Not Allowed', { status: 405, headers: corsHeaders });
        }

        // --- 2. SPA PASSTHROUGH & FIREBASE AUTHENTICATION GUARD ---
        const authHeader = request.headers.get('Authorization');
        
        // If there is no auth header, and the path doesn't look like an explicit API request,
        // we assume it's a human user navigating to the domain. We pass the request to the origin SPA.
        if (!authHeader || !authHeader.startsWith('Bearer ')) {
            if (url.pathname.includes('/telemetry')) {
                return new Response(JSON.stringify({ error: 'Missing or Invalid Authorization Header' }), { 
                    status: 401, headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
                });
            } else {
                // Transparent proxy to origin HTML/assets (Enables Worker to be mapped to main domain!)
                return fetch(request);
            }
        }

        const idToken = authHeader.split('Bearer ')[1];

        if (env.FIREBASE_WEB_API_KEY) {
            try {
                const verifyRes = await fetch(`https://identitytoolkit.googleapis.com/v1/accounts:lookup?key=${env.FIREBASE_WEB_API_KEY}`, {
                    method: 'POST',
                    headers: { 'Content-Type': 'application/json' },
                    body: JSON.stringify({ idToken: idToken })
                });

                const verifyData = await verifyRes.json();

                if (!verifyRes.ok || !verifyData.users || verifyData.users.length === 0) {
                    return new Response(JSON.stringify({ error: 'Unauthorized: Invalid Firebase Token' }), { 
                        status: 401, headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
                    });
                }
            } catch (err) {
                return new Response(JSON.stringify({ error: 'Auth Verification Failed', details: err.message }), { 
                    status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
                });
            }
        }

        // --- 3. FETCH GA4 TELEMETRY DATA (UPGRADED MASS-BATCHING) ---
        let telemetryData = {
            active5m: '--',
            active30m: '--',
            todayUsers: '--',
            wauUsers: '--',
            mauUsers: '--',
            allTimeUsers: '--', 
            todayErrors: '--',
            sevenDayTrend: [0, 0, 0, 0, 0, 0, 0], // Kept for backward compatibility
            chartData: [],                        // Dynamic payload for new Admin UI
            chartLabels: []                       // Dynamic X-Axis labels for new Admin UI
        };

        if (env.GA_PROPERTY_ID && env.GA_CLIENT_EMAIL && env.GA_PRIVATE_KEY) {
            try {
                const token = await getGoogleAccessToken(env.GA_CLIENT_EMAIL, env.GA_PRIVATE_KEY);
                const gaHeaders = { 'Authorization': `Bearer ${token}`, 'Content-Type': 'application/json' };

                // Range parsing for Date-Bucketing Logic (DAU, WAU, MAU, ALL)
                const range = url.searchParams.get('range') || 'DAU';
                let trendDimension = "date";
                let trendStartDate = "6daysAgo";
                let trendEndDate = "today";
                
                if (range === 'WAU') {
                    trendDimension = "isoYearIsoWeek"; // Groups by week
                    trendStartDate = "27daysAgo"; // Fetch 4 weeks
                } else if (range === 'MAU') {
                    trendDimension = "yearMonth"; // Groups by month
                    trendStartDate = "179daysAgo"; // Fetch roughly 6 months
                } else if (range === 'ALL') {
                    trendDimension = "yearMonth";
                    trendStartDate = "2026-01-01"; // Fetch from launch 
                } else {
                    // DAU pagination logic (Default)
                    const weeksAgo = parseInt(url.searchParams.get('weeksAgo')) || 0;
                    const startDays = (weeksAgo * 7) + 6;
                    const endDays = weeksAgo * 7;
                    trendStartDate = `${startDays}daysAgo`;
                    trendEndDate = endDays === 0 ? "today" : `${endDays}daysAgo`;
                }

                // 🚀 Mass Promise.all Execution: Resolves 7 APIs concurrently at the Edge
                const responses = await Promise.all([
                    // Query 0: Realtime 30m
                    fetch(`https://analyticsdata.googleapis.com/v1beta/properties/${env.GA_PROPERTY_ID}:runRealtimeReport`, {
                        method: 'POST', headers: gaHeaders, body: JSON.stringify({ metrics: [{ name: "activeUsers" }] })
                    }),
                    // Query 1: Realtime 5m Sliced
                    fetch(`https://analyticsdata.googleapis.com/v1beta/properties/${env.GA_PROPERTY_ID}:runRealtimeReport`, {
                        method: 'POST', headers: gaHeaders, body: JSON.stringify({ 
                            dimensions: [{ name: "minutesAgo" }], metrics: [{ name: "activeUsers" }] 
                        })
                    }),
                    // Query 2: DAU (Today)
                    fetch(`https://analyticsdata.googleapis.com/v1beta/properties/${env.GA_PROPERTY_ID}:runReport`, {
                        method: 'POST', headers: gaHeaders, body: JSON.stringify({
                            dateRanges: [{ startDate: "today", endDate: "today" }], metrics: [{ name: "activeUsers" }]
                        })
                    }),
                    // Query 3: WAU (7-Day Active)
                    fetch(`https://analyticsdata.googleapis.com/v1beta/properties/${env.GA_PROPERTY_ID}:runReport`, {
                        method: 'POST', headers: gaHeaders, body: JSON.stringify({
                            dateRanges: [{ startDate: "7daysAgo", endDate: "today" }], metrics: [{ name: "activeUsers" }]
                        })
                    }),
                    // Query 4: MAU (28-Day Active)
                    fetch(`https://analyticsdata.googleapis.com/v1beta/properties/${env.GA_PROPERTY_ID}:runReport`, {
                        method: 'POST', headers: gaHeaders, body: JSON.stringify({
                            dateRanges: [{ startDate: "28daysAgo", endDate: "today" }], metrics: [{ name: "activeUsers" }]
                        })
                    }),
                    // Query 5: All Time Total
                    fetch(`https://analyticsdata.googleapis.com/v1beta/properties/${env.GA_PROPERTY_ID}:runReport`, {
                        method: 'POST', headers: gaHeaders, body: JSON.stringify({
                            dateRanges: [{ startDate: "365daysAgo", endDate: "today" }], metrics: [{ name: "activeUsers" }]
                        })
                    }),
                    // Query 6: Dynamic Trend Graph
                    fetch(`https://analyticsdata.googleapis.com/v1beta/properties/${env.GA_PROPERTY_ID}:runReport`, {
                        method: 'POST', headers: gaHeaders, body: JSON.stringify({
                            dateRanges: [{ startDate: trendStartDate, endDate: trendEndDate }],
                            dimensions: [{ name: trendDimension }],
                            metrics: [{ name: "activeUsers" }],
                            orderBys: [{ dimension: { dimensionName: trendDimension } }] 
                        })
                    })
                ]);

                // Check for HTTP errors before consuming JSON
                for (let i = 0; i < responses.length; i++) {
                    if (!responses[i].ok) throw new Error(`GA4 API Request [${i}] Failed: ${await responses[i].text()}`);
                }

                const [
                    realtime30Data, realtime5Data, 
                    dailyData, wauData, mauData, allTimeData, 
                    trendData
                ] = await Promise.all(responses.map(r => r.json()));
                
                // Parse Query 0 (30m)
                telemetryData.active30m = (realtime30Data.rows && realtime30Data.rows[0]) ? parseInt(realtime30Data.rows[0].metricValues[0].value) : 0;
                
                // Parse Query 1 (5m)
                let active5 = 0;
                if (realtime5Data.rows) {
                    realtime5Data.rows.forEach(row => {
                        const minuteStr = row.dimensionValues[0].value; 
                        const users = parseInt(row.metricValues[0].value) || 0;
                        if (parseInt(minuteStr, 10) < 5) active5 += users;
                    });
                }
                telemetryData.active5m = active5; 

                // Parse Core Totals
                telemetryData.todayUsers = (dailyData.rows && dailyData.rows[0]) ? parseInt(dailyData.rows[0].metricValues[0].value) : 0;
                telemetryData.wauUsers = (wauData.rows && wauData.rows[0]) ? parseInt(wauData.rows[0].metricValues[0].value) : 0;
                telemetryData.mauUsers = (mauData.rows && mauData.rows[0]) ? parseInt(mauData.rows[0].metricValues[0].value) : 0;
                telemetryData.allTimeUsers = (allTimeData.rows && allTimeData.rows[0]) ? parseInt(allTimeData.rows[0].metricValues[0].value) : 0;

                // Parse Dynamic Trend Chart Data
                let activeCountsArray = [];
                let labelsArray = [];
                
                if (trendData.rows) {
                    activeCountsArray = trendData.rows.map(row => parseInt(row.metricValues[0].value) || 0);
                    labelsArray = trendData.rows.map(row => row.dimensionValues[0].value);
                }
                
                // Maintain backward compatibility for Phase 6 Admin UI until Phase 8 drops
                if (range === 'DAU' || !url.searchParams.has('range')) {
                    telemetryData.sevenDayTrend = [...Array(7).fill(0), ...activeCountsArray].slice(-7);
                }

                // New arrays directly consumed by the Phase 8 scalable chart
                telemetryData.chartData = activeCountsArray;
                telemetryData.chartLabels = labelsArray;

            } catch (e) {
                console.error("GA4 Fetch Error:", e.message);
                telemetryData.active5m = "ERR";
                telemetryData.active30m = "ERR";
                telemetryData.todayUsers = "ERR";
                telemetryData.wauUsers = "ERR";
                telemetryData.mauUsers = "ERR";
                telemetryData.allTimeUsers = "ERR"; 
                telemetryData.gaErrorDetail = e.message; 
            }
        }

        // --- 4. FETCH SENTRY TELEMETRY DATA ---
        if (env.SENTRY_AUTH_TOKEN && env.SENTRY_ORG && env.SENTRY_PROJECT) {
            try {
                const sentryRes = await fetch(`https://sentry.io/api/0/projects/${env.SENTRY_ORG}/${env.SENTRY_PROJECT}/stats/?stat=received&resolution=1d`, {
                    headers: { 'Authorization': `Bearer ${env.SENTRY_AUTH_TOKEN}` }
                });

                if (!sentryRes.ok) {
                    const errText = await sentryRes.text();
                    throw new Error(`Sentry API Rejected: ${errText}`);
                }

                const sentryData = await sentryRes.json();
                
                if (Array.isArray(sentryData) && sentryData.length > 0) {
                    const lastDataPoint = sentryData[sentryData.length - 1];
                    telemetryData.todayErrors = parseInt(lastDataPoint[1]) || 0;
                } else {
                    telemetryData.todayErrors = 0;
                }
            } catch (e) {
                console.error("Sentry Fetch Error:", e.message);
                telemetryData.todayErrors = "ERR"; 
                telemetryData.sentryErrorDetail = e.message; 
            }
        }

        // --- 5. RETURN SECURE PAYLOAD ---
        return new Response(JSON.stringify(telemetryData), {
            status: 200,
            headers: { ...corsHeaders, 'Content-Type': 'application/json' }
        });
    }
};